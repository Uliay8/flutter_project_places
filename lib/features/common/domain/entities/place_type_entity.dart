/// Enum, представляющий типы мест.
/// Используется для классификации мест и упрощает их фильтрацию и отображение.
enum PlaceTypeEntity {
  /// Другой тип места, не попадающий ни под одну из других категорий.
  other,

  /// Парк.
  park,

  /// Памятник.
  monument,

  /// Театр.
  theatre,

  /// Музей.
  museum,

  /// Храм.
  temple,

  /// Отель.
  hotel,

  /// Ресторан.
  restaurant,

  /// Кафе.
  cafe,

  /// Торговый центр/магазин.
  shopping,
}
